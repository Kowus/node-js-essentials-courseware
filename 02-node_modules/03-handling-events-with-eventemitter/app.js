// Create an object to inherit the event emitter

var EventEmitter = require('events').EventEmitter;
var util = require('util');

var Person = function (name) {
    this.name = name;
};

util.inherits(Person, EventEmitter);

var jerome = new Person("Jerome Jackson");

jerome.on('speech', function (speech) {
    console.log(`${this.name} said: ${speech}`);
});

jerome.emit('speech', "You're smart! Figure it out!");