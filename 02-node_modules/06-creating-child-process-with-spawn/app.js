var spawn = require('child_process').spawn;

cp = spawn('node', ['./talkative']);

cp.stdout.on("data", function (data) {
    console.log(`STDOUT: ${data.toString()}`);
});

cp.on("close", function () {
    console.log("Child process has ended");
    process.exit();
});

setTimeout(function () {
    cp.stdin.write("stOp");
}, 10000);