var Person = require('./lib/person');

var jerome = new Person("Jerome Jackson");
var chelsey = new Person("Chelsey Roebuck");

chelsey.on('speech', function (speech) {
    console.log(`${this.name}: ${speech}`)
});
jerome.on('speech', function (speech) {
    console.log(`${this.name}: ${speech}`);
});

jerome.emit('speech', "You're smart! Figure it out!");
chelsey.emit('speech', "I guess the man with a drill is pretty powerful.");